// Get skills by category
export const getSkillsByCategory = (category) => {
  const categoryMap = {
    DevOps: skills.devops,
    Frontend: skills.frontend,
    Backend: skills.backend,
    "AI/ML": skills.aiml,
    Tools: skills.tools,
    All: getAllSkills(),
  };

  return categoryMap[category] || [];
};

